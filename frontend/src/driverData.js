export const getDriverImage = (driverName) => {
  if (!driverName) return null;
  
  // First, check if it's a driver abbreviation (3 letters, typically uppercase)
  // and get the full name from driverInfo
  let fullName = driverName;
  const upperName = driverName.toUpperCase();
  if (driverInfo[upperName] && driverInfo[upperName].fullName) {
    fullName = driverInfo[upperName].fullName;
  }
  
  // Try exact match with full name
  if (driverImages[fullName]) {
    return driverImages[fullName];
  }
  
  // Try exact match with original name (in case it's already a full name)
  if (driverImages[driverName]) {
    return driverImages[driverName];
  }
  
  // Try case-insensitive match with full name
  const lowerFullName = fullName.toLowerCase();
  for (const [key, value] of Object.entries(driverImages)) {
    if (key.toLowerCase() === lowerFullName) {
      return value;
    }
  }
  
  // Try case-insensitive match with original name
  const lowerName = driverName.toLowerCase();
  for (const [key, value] of Object.entries(driverImages)) {
    if (key.toLowerCase() === lowerName) {
      return value;
    }
  }
  
  return null;
};

export const getDriverInfo = (driverName) => {
  // Try exact match first
  if (driverInfo[driverName]) {
    return driverInfo[driverName];
  }
  
  // Try case-insensitive match
  const upperName = driverName.toUpperCase();
  for (const [key, value] of Object.entries(driverInfo)) {
    if (key.toUpperCase() === upperName) {
      return value;
    }
  }
  
  return null;
};

export const getTeamColor = (constructorName) => {
  if (!constructorName) return teamColors['Unknown'];
  
  // Try exact match
  if (teamColors[constructorName]) {
    return teamColors[constructorName];
  }
  
  // Try partial match
  const upperConstructor = constructorName.toUpperCase();
  for (const [key, value] of Object.entries(teamColors)) {
    if (key.toUpperCase().includes(upperConstructor) || upperConstructor.includes(key.toUpperCase())) {
      return value;
    }
  }
  
  return teamColors['Unknown'];
};

